enum Shape {
    Rock,
    Paper,
    Scissors,
}

struct Round {
    opponent: Shape,
    outcome: Outcome,
}

enum Outcome {
    Win,
    Lose,
    Draw,
}

impl std::str::FromStr for Round {
    type Err = std::convert::Infallible;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        let mut split = s.split_whitespace();
        let opponent = split.next().unwrap();
        let outcome = split.next().unwrap();

        let opponent = match opponent {
            "A" => Shape::Rock,
            "B" => Shape::Paper,
            "C" => Shape::Scissors,
            _ => unreachable!(),
        };

        let outcome = match outcome {
            "X" => Outcome::Lose,
            "Y" => Outcome::Draw,
            "Z" => Outcome::Win,
            _ => unreachable!(),
        };

        Ok(Round { opponent, outcome })
    }
}

impl Round {
    fn score(&self) -> u32 {
        let my_shape = match self.opponent {
            Shape::Rock => match self.outcome {
                Outcome::Win => Shape::Paper,
                Outcome::Lose => Shape::Scissors,
                Outcome::Draw => Shape::Rock,
            },
            Shape::Paper => match self.outcome {
                Outcome::Win => Shape::Scissors,
                Outcome::Lose => Shape::Rock,
                Outcome::Draw => Shape::Paper,
            },
            Shape::Scissors => match self.outcome {
                Outcome::Win => Shape::Rock,
                Outcome::Lose => Shape::Paper,
                Outcome::Draw => Shape::Scissors,
            },
        };
        let shape_score = match my_shape {
            Shape::Rock => 1,
            Shape::Paper => 2,
            Shape::Scissors => 3,
        };
        let outcome_score = match self.outcome {
            Outcome::Win => 6,
            Outcome::Lose => 0,
            Outcome::Draw => 3,
        };
        shape_score + outcome_score
    }
}

