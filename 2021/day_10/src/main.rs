fn corrupted_score(line: &[u8]) -> Option<u32> {
    let mut stack = vec![];
    for c in line {
        match c {
            b'(' => stack.push(b')'),
            b'[' => stack.push(b']'),
            b'{' => stack.push(b'}'),
            b'<' => stack.push(b'>'),
            b')' | b']' | b'}' | b'>' => {
                if stack.pop() != Some(*c) {
                    return Some(match c {
                        b')' => 3,
                        b']' => 57,
                        b'}' => 1197,
                        b'>' => 25137,
                        _ => unreachable!(),
                    });
                }
            }
            _ => unreachable!(),
        }
    }
    None
}

fn complete_score(line: &[u8]) -> Option<u64> {
    let mut stack = vec![];
    for c in line {
        match c {
            b'(' => stack.push(b')'),
            b'[' => stack.push(b']'),
            b'{' => stack.push(b'}'),
            b'<' => stack.push(b'>'),
            b')' | b']' | b'}' | b'>' => {
                if stack.pop() != Some(*c) {
                    return None;
                }
            }
            _ => unreachable!(),
        }
    }

    let mut result = 0;
    while let Some(c) = stack.pop() {
        result = result * 5
            + match c {
                b')' => 1,
                b']' => 2,
                b'}' => 3,
                b'>' => 4,
                _ => unreachable!(),
            };
    }
    Some(result)
}
