fn main() {
    let mut num_ones = [0usize; 12];
    for number in INPUT {
        for (digit, num_ones) in number.iter().zip(num_ones.iter_mut()) {
            if *digit == b'1' {
                *num_ones += 1;
            }
        }
    }

    let mut gamma = 0;
    let mut epsilon = 0;
    for (i, num_ones) in num_ones.iter().rev().enumerate() {
        let value = 1 << i;
        if *num_ones >= INPUT.len() - num_ones {
            gamma |= value;
        } else {
            epsilon |= value;
        }
    }
    println!("{}", gamma * epsilon);

    let mut oxygen = INPUT.to_vec();
    let mut co2 = oxygen.clone();
    for bit_index in 0..num_ones.len() {
        bit_filter(bit_index, &mut oxygen, Policy::KeepMoreOr1);
        bit_filter(bit_index, &mut co2, Policy::KeepLessOr0);
    }
    println!("{}", parse(oxygen[0]) * parse(co2[0]));
}

enum Policy {
    KeepMoreOr1,
    KeepLessOr0,
}

fn bit_filter(index: usize, list: &mut Vec<&'static [u8; 12]>, policy: Policy) {
    if list.len() == 1 {
        return;
    }

    let num_ones = list.iter().filter(|number| number[index] == b'1').count();
    match policy {
        Policy::KeepMoreOr1 => {
            if num_ones >= list.len() - num_ones {
                list.retain(|number| number[index] == b'1');
            } else {
                list.retain(|number| number[index] == b'0');
            }
        }
        Policy::KeepLessOr0 => {
            if num_ones < list.len() - num_ones {
                list.retain(|number| number[index] == b'1');
            } else {
                list.retain(|number| number[index] == b'0');
            }
        }
    }
}

fn parse(number: &'static [u8; 12]) -> u32 {
    let mut result = 0;
    for (i, value) in number.iter().rev().enumerate() {
        if *value == b'1' {
            result |= 1 << i;
        }
    }
    result
}
